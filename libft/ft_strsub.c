#include "includes/libft.h"

char	*ft_strsub(char const *s, unsigned int start, size_t len)
{
	char			*str;
	unsigned int	i;

	if (s)
	{
		i = 0;
		str = ft_strnew(len);
		if (str)
		{
			while (i < len)
			{
				str[i] = s[start + i];
				i++;
			}
		}
		return (str);
	}
	return (NULL);
}
