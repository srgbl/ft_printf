#include "includes/libft.h"

char	*ft_strncpy(char *dst, const char *src, size_t len)
{
	size_t	n;

	n = 0;
	while (*src && len > 0)
	{
		*dst = *src;
		dst++;
		src++;
		len--;
		n++;
	}
	while (len > 0)
	{
		*dst = '\0';
		dst++;
		len--;
		n++;
	}
	return (dst - n);
}
